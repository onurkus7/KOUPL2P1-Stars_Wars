
public class Lokasyon {
    
    private int lokasyon_x;
    private int lokasyon_y;

    public Lokasyon(int lokasyon_x, int lokasyon_y) {
        this.lokasyon_x = lokasyon_x;
        this.lokasyon_y = lokasyon_y;
    }

    public int getLokasyon_x() {
        return lokasyon_x;
    }

    public void setLokasyon_x(int lokasyon_x) {
        this.lokasyon_x = lokasyon_x;
    }

    public int getLokasyon_y() {
        return lokasyon_y;
    }

    public void setLokasyon_y(int lokasyon_y) {
        this.lokasyon_y = lokasyon_y;
    }
    
    
    
}
